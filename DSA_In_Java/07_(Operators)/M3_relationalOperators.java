public class M3_relationalOperators {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);

        boolean c = true;
        boolean d = false;
        System.out.println(c == d);
        System.out.println(c != d);
    }
}
